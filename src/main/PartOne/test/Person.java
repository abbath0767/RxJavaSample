package PartOne.test;

import rx.Observable;
import rx.Subscriber;

public class Person implements Observable.OnSubscribe<Person> {

    private String name;

    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public void call(Subscriber<? super Person> subscriber) {
        System.out.println("call in Person! new person create in subscriber!");
        subscriber.onNext(this);
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
